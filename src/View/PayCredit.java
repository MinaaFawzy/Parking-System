    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String credit=creditNumber.getText();
        if (credit.isEmpty())
        {
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "You must enter your credit number");
        }
        else if (credit.length()<14)
        {
            Component frame = null;
            JOptionPane.showMessageDialog(frame, "Number must be 14 digit");
        }else
        {
            try {
                int id= getId();
                System.out.println(id);
                Component frame = null;
                JOptionPane.showMessageDialog(frame, "Payment has been successfully");
                _paymentType.pay(id);
                new HomePage().setVisible(true);
                this.setVisible(false);
            } catch (HeadlessException ex) {
                System.out.println(ex);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

